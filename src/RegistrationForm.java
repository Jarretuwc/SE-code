    private void ckbShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ckbShowPasswordActionPerformed
        // Show/hide password and confirm password
        if(ckbShowPassword.isSelected()){
            tfPasswordReg.setEchoChar((char)0);
            tfConfirm.setEchoChar((char)0);
        }else{
            tfPasswordReg.setEchoChar('*');
            tfConfirm.setEchoChar('*');
        }
       
    }//GEN-LAST:event_ckbShowPasswordActionPerformed

    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        //Check that none of the fields are empty
        if(tfFirstName.getText().equals("")){
         lblVerify.setText("Missing Fields");
     }else if (tfLastName.getText().equals("")){
         lblVerify.setText("Missing Fields");
     }else if(tfEmail2.getText().equals("")){
         lblVerify.setText("Missing Fields");
     }else if(tfPasswordReg.getText().equals("")){
         lblVerify.setText("Missing Fields");
     }else if(tfConfirm.getText().equals("")){
         lblVerify.setText("Missing Fields");
     }else if(tfEmail2.getText().equals("")){
         lblVerify.setText("Missing Fields");
     }else if (!tfConfirm.getText().equals(tfPasswordReg.getText())) {
         lblVerify.setText("Passwords do not match");
     }else{
         addRow();
         lblVerify.setText("successs");
         //LoginForm lf =new LoginForm();
         //lf.setVisible(true);
        // setVisible(false);
         
         
     }
    }//GEN-LAST:event_btnRegisterActionPerformed

